package bookadventure.exceptions;

/**
 *
 * @author Tashakrn9
 */
public class ViewLayerClassException extends Exception {

    public ViewLayerClassException() {
    }

    public ViewLayerClassException(String message) {
        super(message);
    }

    public ViewLayerClassException(String message, Throwable cause) {
        super(message, cause);
    }

    public ViewLayerClassException(Throwable cause) {
        super(cause);
    }

    public ViewLayerClassException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
    
}

    